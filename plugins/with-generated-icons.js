const { withPlugins } = require("expo/config-plugins");
const fs = require("fs");
const path = require("path");

/**
 * Expo Config Plugin: Generates adaptive icon files if they don't exist
 * This runs during the prebuild phase, before Android build starts
 */
module.exports = function withGeneratedIcons(config) {
  return withPlugins(config, [
    [
      function (config) {
        // Ensure icons exist before build
        const assetsDir = path.join(process.cwd(), "assets", "images");
        
        if (!fs.existsSync(assetsDir)) {
          fs.mkdirSync(assetsDir, { recursive: true });
        }

        const iconFiles = {
          "android-icon-foreground.png": createForegroundIcon(),
          "android-icon-background.png": createBackgroundIcon(),
          "android-icon-monochrome.png": createMonochromeIcon(),
        };

        Object.entries(iconFiles).forEach(([filename, buffer]) => {
          const filePath = path.join(assetsDir, filename);
          if (!fs.existsSync(filePath)) {
            console.log(`[with-generated-icons] Creating ${filename}`);
            fs.writeFileSync(filePath, buffer);
          }
        });

        console.log("[with-generated-icons] Icon generation complete");
        return config;
      },
    ],
  ]);
};

